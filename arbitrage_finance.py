BY_ID = {f["id"]: f for f in FEATURES}


def run(feature_id: int, user_input: str = "") -> str:
    f = BY_ID.get(feature_id)
    if not f:
        return "ميزة غير موجودة."
    if f["kind"] == "gated":
        return "⛔ موقوف لأسباب امتثال/سلامة.\n" + f.get("note", "")
    if f["kind"] == "scaffold":
        return "🔌 " + f.get("note", "تحتاج ربطاً خارجياً.") + DISCLAIMER
    if f["kind"] == "tool" and feature_id == 121:
        d = database.roi_dashboard()
        if not d["totals_mixed"]:
            return "لا توجد بيانات دخل بعد. سجّل قنوات الدخل لتظهر لوحة ROI." + DISCLAIMER
        lines = "\n".join(f"- {k}: {v}" for k, v in d["totals_mixed"].items())
        return (f"🏆 الأعلى عائداً: {d['best_stream']}\n{lines}" + DISCLAIMER)
    if f["kind"] == "search":
        from search import web_search
        rows = web_search(f"{user_input.strip() or 'crypto airdrop'} legit airdrop guide", 5)
        return (("\n".join(f"- {r['title']} — {r['url']}" for r in rows if r.get("url"))
                 or "مفيش نتائج دلوقتي.") + DISCLAIMER)
    return ai_core.llm(f["instruction"], user_input, f.get("mode", "professional")) + DISCLAIMER
