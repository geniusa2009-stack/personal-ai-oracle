def dynamic_pricing(text: str) -> str:
    """حاسبة تسعير: اكتب 3 أرقام = ميزانية العميل، متوسط السوق، متوسط المنافسين."""
    nums = [float(x) for x in re.findall(r"\d+(?:\.\d+)?", text)]
    if len(nums) < 2:
        return "اكتب الأرقام: «ميزانية العميل، متوسط السوق، متوسط المنافسين». مثال: 500, 400, 380"
    budget = nums[0]
    market = nums[1]
    competitor = nums[2] if len(nums) > 2 else market * 0.95
    # سعر يوازن بين عدم ترك ربح وعدم تجاوز الميزانية، وتفوّق طفيف في القيمة
    floor = market * 0.8
    target = min(budget * 0.9, market * 1.1)
    recommended = max(floor, min(target, competitor * 1.05))
    return (f"💡 السعر المقترح: {recommended:.0f}\n"
            f"- ميزانية العميل: {budget:.0f} | متوسط السوق: {market:.0f} | المنافسون: {competitor:.0f}\n"
            f"- النطاق الآمن: {floor:.0f} – {budget:.0f}\n"
            f"النصيحة: قدّم القيمة (نتائج/سرعة/ضمان) لتبرير السعر بدل المنافسة على الأرخص.")


def run(feature_id: int, user_input: str = "") -> str:
    f = BY_ID.get(feature_id)
    if not f:
        return "ميزة غير موجودة."
    if f["kind"] == "gated":
        return "⛔ موقوف لأسباب امتثال.\n" + f.get("note", "")
    if f["kind"] == "tool" and feature_id == 11:
        return dynamic_pricing(user_input)
    return ai_core.llm(f["instruction"], user_input, f.get("mode", "professional"))
