BY_ID = {f["id"]: f for f in FEATURES}


def run(feature_id: int, user_input: str = "") -> str:
    f = BY_ID.get(feature_id)
    if not f:
        return "ميزة غير موجودة."
    if f["kind"] == "gated":
        return "⛔ موقوف لأسباب امتثال.\n" + f.get("note", "")
    if f["kind"] == "scaffold":
        return "🔌 " + f.get("note", "تحتاج ربطاً خارجياً.")
    if f["kind"] == "tool":
        if feature_id == 31:
            res = income_hunter.compile_ebook(user_input or "دليل الفرص", "scholarships")
            return f"✅ اتعمل كتاب فيه {res['items']} عنصر.\nالملف: {res['path']}"
        if feature_id == 36:
            pl = income_hunter.create_payment_link(user_input or "منتج رقمي", 9.99)
            return pl.get("url") or pl.get("note", "اضبط رابط الدفع.")
        if feature_id == 41:
            return income_hunter.sales_funnel_reply(user_input or "بكام المنتج؟")
    return ai_core.llm(f["instruction"], user_input, f.get("mode", "professional"))
